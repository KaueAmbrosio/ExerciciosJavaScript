console.log("=== EXEMPLO TIPOS ====");

// String -> Textp
let texto = "Hello world!";

// Number -> Numeros(inteiros ou decimais) 
let numero = 10;
let decimal = 5.5;

// boolean -> boleano(verdadeiro ou falso)

let verdadeiro = true;
let falso = false;

// Null -> valor vazio intencional
let vazio = null;

// Undifined -> variavel declarada mas sem valor
let indefinido;

console.log("Tipo de Texto:",typeof texto);
console.log("Tipo de Numero:", typeof numero);
console.log("Tipo de Decimal:", typeof decimal);
console.log("Tipo de Booleano:", typeof verdadeiro);
console.log("Tipo de Null:",typeof vazio);
console.log("Tipo de UndefinedN:",typeof indefinido);
